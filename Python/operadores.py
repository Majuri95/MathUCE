"""Operadores en Python
operadores aritméticos"""
a=2
b=3
suma=a+b#esto es una resta
print("la suma de:", a, "más", b, "es:", suma)
resta=a-b#esto es una resta
print("la resta de:", a, "menos", b, "es:", resta)
multiplicación=a*b#esto es una multiplicación
print("la multiplicación de:", a, "multiplicado", b, "es:", multiplicación)
división=a/b#esto es una división
print("la división de:", a, "dividido", b, "es:", división)
potenciación=a**b#es una potenciación
print("la potenciación de:", a, "elevado", b, "es:", potenciación)

